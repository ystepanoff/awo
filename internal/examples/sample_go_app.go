const sampleCalculatorGo = `// Package calculator is a tiny pure-arithmetic helper used as the
// AWO sample fixture. It is intentionally simple so dogfooding runs
// stay fast and deterministic.
package calculator

// Add returns a + b.
func Add(a, b int) int { return a + b }

// Sub returns a - b.
func Sub(a, b int) int { return a - b }

// Mul returns a * b.
func Mul(a, b int) int { return a * b }

// Divide returns a / b. It panics when b == 0; that is an
// intentional edge case the AWO sample task asks agents to expose
// with a test before fixing.
func Divide(a, b int) int {
	return a / b
}
`

const sampleCalculatorTestGo = `package calculator

import "testing"

func TestAdd(t *testing.T) {
	if got := Add(2, 3); got != 5 {
		t.Errorf("Add(2,3)=%d want 5", got)
	}
}

func TestSub(t *testing.T) {
	if got := Sub(10, 4); got != 6 {
		t.Errorf("Sub(10,4)=%d want 6", got)
	}
}

func TestMul(t *testing.T) {
	if got := Mul(6, 7); got != 42 {
		t.Errorf("Mul(6,7)=%d want 42", got)
	}
}

func TestDivideHappy(t *testing.T) {
	if got := Divide(10, 2); got != 5 {
		t.Errorf("Divide(10,2)=%d want 5", got)
	}
}

// NOTE: Divide(_, 0) is intentionally not tested here. The AWO demo
// task is to add edge-case coverage that exposes the missing
// behavior.
`
