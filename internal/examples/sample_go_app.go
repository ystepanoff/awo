package examples

// sampleGoAppFiles holds the deterministic file content for the
// fixture. Keys are paths relative to the fixture root. Values are
// fixed strings — generation must produce byte-identical output across
// runs so users can diff against a known baseline.
//
// The calculator intentionally does not handle division by zero: that
// is the "obvious test improvement opportunity" agents are expected to
// notice when run against this fixture. The existing tests pass; new
// tests should be added to expose the missing edge case before
// changing the implementation.
var sampleGoAppFiles = map[string]string{
	".awo-fixture": awoFixtureMarker,
	"README.md":    sampleReadme,
	"go.mod":       sampleGoMod,
	".gitignore":   sampleGitignore,

	"calculator.go":      sampleCalculatorGo,
	"calculator_test.go": sampleCalculatorTestGo,
}

const awoFixtureMarker = `This directory is an AWO fixture. AWO will refuse to overwrite it
without --force, and will refuse to delete any directory at this path
that does not contain this marker file.
`

